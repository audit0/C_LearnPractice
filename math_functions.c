#include <stdio.h>
#include <math.h> //* The math header file

int main()
{
    //! Square Roots
    printf("%f\n", sqrt(9)); // sqrt() function

    //! Rounding
    printf("%f\n", ceil(1.4)); //* Will round a number upwards to nearest integer
    printf("%f\n", floor(1.4)); //* Will round a number downwards to nearest lowest integer

    //! Power
    printf("%f\n", pow(4,3)); //* Returns value 1 to the power of value 2.

    return 0;
}